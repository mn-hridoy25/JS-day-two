// comparison operators list:-
// 1. ==
// 2. >
// 3. <
// 4. !=
// 5. >=
// 6. <=

var a = 20;
var b = 30;

// example:
console.log(a == b); // a ar b soman hole true return korbe
console.log(a > b); // a theke b suto hole true return korbe
console.log(a < b); // a theke b boro hole true return korbe
console.log(a != b); // a ar b soman na hole true return korbe
console.log(a >= b); // a jodi b er theke boro othoba soman hoy tobe true return korbe
console.log(a <= b); // a jodi b er theke suto othoba soman hoy tobe true return korbe

